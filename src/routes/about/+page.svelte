    <h1>About</h1>
    <p>
        <span class="codeblock">bosp</span> is a small portal of utilities for developers
        that would otherwise need to create on their own or use (and trust) from
        someone else.
    </p>
    <p>
        What's the difference to having a bunch of tabs? That this toolkit is
        free to fork from Github so you can change whaterver you want: from
        removing tools to just changing the font style. It is designed to be
        used as is or to be modified at pleasure.
    </p>
    <p>
        It uses the <a href="https://kit.svelte.dev"
            >WONDERFUL SvelteKit framework</a
        >
        and, alongside their own vision for the project, pretends to be as minimal
        in code as possible.
    </p>
    <p>
        After all, <span class="quote"
            >simplicity is a prerequisite for reliability (Edsger Djikstra).</span
        >
    </p>
